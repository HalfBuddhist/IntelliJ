package com.lqw.lottery;

public class IndexRecord {

	private String name;
	private float finalWinPei;
	private float finalPKPei;
	private float finalLosePei;
	private float returnRate;


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public float getFinalWinPei() {
		return finalWinPei;
	}

	public void setFinalWinPei(float finalWinPei) {
		this.finalWinPei = finalWinPei;
	}

	public float getFinalPKPei() {
		return finalPKPei;
	}

	public void setFinalPKPei(float finalPKPei) {
		this.finalPKPei = finalPKPei;
	}

	public float getFinalLosePei() {
		return finalLosePei;
	}

	public void setFinalLosePei(float finalLosePei) {
		this.finalLosePei = finalLosePei;
	}

	public float getReturnRate() {
		return returnRate;
	}

	public void setReturnRate(float returnRate) {
		this.returnRate = returnRate;
	}
	
	
	

}
